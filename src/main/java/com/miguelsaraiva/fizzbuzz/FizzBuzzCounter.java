package com.miguelsaraiva.fizzbuzz;

public class FizzBuzzCounter {

	private int totalFizz = 0;
	private int totalBuzz = 0;
	private int totalFizzBuzz = 0;
	private int totalLucky = 0;
	private int totalInteger = 0;
	
	public int getTotalFizz() {
		return totalFizz;
	}
	public void setTotalFizz(int totalFizz) {
		this.totalFizz = totalFizz;
	}
	public int getTotalBuzz() {
		return totalBuzz;
	}
	public void setTotalBuzz(int totalBuzz) {
		this.totalBuzz = totalBuzz;
	}
	public int getTotalFizzBuzz() {
		return totalFizzBuzz;
	}
	public void setTotalFizzBuzz(int totalFizzBuzz) {
		this.totalFizzBuzz = totalFizzBuzz;
	}
	public int getTotalLucky() {
		return totalLucky;
	}
	public void setTotalLucky(int totalLucky) {
		this.totalLucky = totalLucky;
	}
	public int getTotalInteger() {
		return totalInteger;
	}
	public void setTotalInteger(int totalInteger) {
		this.totalInteger = totalInteger;
	}
	
}
